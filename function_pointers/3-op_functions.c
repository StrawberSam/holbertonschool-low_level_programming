#include "3-calc.h"

/**
 *op_add - addition de a avec b
 *@a: variable
 *@b: variable
 *Return: the result of the op
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - soustraction de a avec b
 *@a: variable
 *@b: variable
 *Return: the result of the op
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - multiplication de a avec b
 *@a: variable
 *@b: variable
 *Return: the result of the op
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *op_div - division de a avec b
 *@a: variable
 *@b: variable
 *Return: the result of the op
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *op_mod - reste de la division de a avec b
 *@a: variable
 *@b: variable
 *Return: the result of the op
 */

int op_mod(int a, int b)
{
	return (a % b);
}
